# Introduction to Number Systems
# This lesson will be an introduction to number systems. Here we see the different types of number systems
# that are represented in mathematics.

# A number system is a writing system where digits and symbols are used in a consistent manner to represent values.

# The same sequence of symbols may represent different numbers in different number systems.

# There are various types of number systems in mathematics, like binary, decimal, octal, and hexadecimal.

