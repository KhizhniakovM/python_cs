# ? Sample answers 
# Q: “When things go wrong, how do you deal with it? Give me an example of a situation you 
# dealt with in a previous role.”

# I was working as an App Developer for XYZ Company when a Zero-Day Privilege Elevation vulnerability 
# was discovered. Microsoft issued a patch and we had 6000 Windows servers running in 4 different 
# data centers that all needed to be patched as quickly and efficiently as possible (Situation). 
# It wasn’t my job to patch servers, but the VP asked all of us for ideas to get the work done 
# faster (Tasks). I suggested that several of us could help do validation if the procedure was 
# documented. Others could help with tracking the servers coming in and out of rotation and 
# updating tickets. This freed up the Systems Engineers to do the work that only they could do 
# (Actions). I had never done some of those tasks before, so I had to follow procedures exactly 
# and ask for help if I wasn’t sure about something. I was willing to take directions and learn. 
# I took good notes and helped make the documentation easier to follow (Results). I gained a new 
# appreciation for the Systems and Security Engineers. We worked long hours, but it felt good to 
# pull together. It was also good to know that we could do that again if a serious security issue came up.

