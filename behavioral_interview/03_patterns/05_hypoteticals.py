# Hypotheticals
# In this lesson, we'll learn all about hypothetical questions and how to best answer them.

# * MARK: - Introduction
# Hypotheticals, or “what if” questions, challenge you to demonstrate creativity and confidence;
# they also allow interviewers to assess your instinctive responses. These scenarios may be
# realistic, fantastical, or both.

# Being ready for hypothetical questions can help you get past personal bias that may affect your
# interview performance. For example, if you are someone who prefers realism, you may be thrown
# by questions that ask you to imagine unrealistic scenarios. However, if you know that
# hypotheticals ​are coming, preparing for them will ensure that you won’t be caught off guard
# and respond poorly.

# Parsing hypothetical questions
# Hypothetical questions will begin with phrases like:

# - “Imagine…”
# - “If…”
# - “Put yourself in the position of…”
# - “If you could have…”
# - “It’s 2050 and…”
# - “You’re in charge of x,y,z and…”

# Pro Tip: If you are someone who loves details and accuracy, you will be tempted to ask for
# specifics before answering. Resist this urge! This is a hypothetical situation, not a real one.
# If you need specifics, state them as your assumptions. For example, “I will assume that the
# team is co-located,” or “I will say it’s springtime​ and temperatures outside are moderate.”

# Let’s take a look at an example.

# Question
# If I were your supervisor and asked you to do something that you disagreed with, what would you do?

# Response
# "If my supervisor asked me to do something I disagreed with I would respectfully push back. I
# would ask questions about the value of the project/feature/task (etc.) and how it would benefit
# the greater good of users. It’s important to me that I believe in what I’m working on and if I
# disagree with it, I think that it’s important and appropriate to push back against my management
# for further explanation.” – Dakota

# Pro Tip: You can take advantage of hypothetical questions to highlight specific skills that you
# know will be beneficial to the organization (hence why it is important to do your homework!).

# In the example below, you will see two different responses to the same question that highlight
# different talents.

# Question
# How would you design a field test for wearable devices used to track dinosaurs in Jurassic Park?

# Response 1
# What a fun question! I would do an exhaustive pre-test to maximize the testing that can be
# done in a safe way. I would assemble a team that includes the animal caretakers and experts,
# someone from park operations and a device testing expert. I would get as much input as possible
# from the team I curated. I would plan to test the devices on humans first, then take the trial
# to the zoo to test on animals that are non-threatening. With a series of graduated testing,
# we could validate our processes for data collection, support, provisioning, and problem management.
# Lower level testing could verify device operations within humidity and temperature ranges.

# This response highlights the candidate’s ability to plan and solicit advice from experts.

# Pro Tip: It’s OK to think out loud when you’re answering hypothetical questions. In fact,
# it’s better so that the interviewer can see the way your brain works.

# Response 2
# I love this question, Jurassic Park is my favorite movie! I would gather all my friends with pets.
# We would plan a series of three weekly pet playdates at the city park for 2 hours each. The testing
# team would arrive at the park early to set up all their equipment and outfit the pets. With each
# weekly iteration, we would deal with problems or miscalculations that we found. Then, I would talk
# to my father who works at a construction company that is currently building a high-rise downtown.
# I would ask if we could put devices on the construction workers and some of the equipment for three
# straight days. The devices should be exposed to the elements as much as possible. After completing
# the four tests, I would gather, compare, and analyze the test results with the team.

# This response highlights​ the candidate’s creativity and ability to succeed with limited resources.
# These talents would be great to highlight if you are interviewing at a small company or start-up.

# Why interviewers ask hypotheticals
# Interviewers ask hypothetical questions to learn more about the way you creatively think, process, and react.

# Take a look at this question created by Salifu who is a talent leader at GitHub. He does not
# answer the question, but instead, he tells you what he is listening for as the interviewer.

# Q: Within a 5-minute time span, the following people come to you asking for help: A V.P.
# whom you do not regularly interact with, your manager, and a customer. All three say their
# need is urgent. How do you prioritize them?

# What interviewers listening for?

# Motivation
# When I ask this question, I’m less concerned with the candidate’s answer and more focused on their
# thought process. The HOWs and WHYs are the keys for me. Anecdotally, when I’ve asked this question,
# the majority of the candidates prioritize in the following order: V.P., manager, then the customer.
# Candidates rarely dig deeper to find out what the requests are. Instead, they prioritize
# based on titles alone.

# I’m mainly looking for two things in the candidate’s response. Thought process and effective communication.

# Thought process
# Does the candidate ask for more information to make an informed decision on how to prioritize?
# What if the issue the V.P. is facing is the inability to connect their bluetooth speaker to
# their laptop so they can listen to music in their office? Is that truly more important than a
# customer needing help? After hearing the needs, are they able to correctly prioritize based
# on the information they’ve received?

# Communication
# Do they show that they are able to communicate with people at different levels? Moreover,
# are they able to deliver “bad” news to people at different levels? Will they let everyone know
# how and when they plan to address their needs?

# This is my favorite question because you can go in a number of different ways depending on how
# the interview is going. The other added benefit is that it works with candidates at almost every level.
